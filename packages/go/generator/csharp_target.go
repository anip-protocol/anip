func BuildCSharpProject(definition *AnipServiceDefinition, options BuildCSharpProjectOptions) (*GeneratedProject, error) {
	model, err := BuildGenerationModel(definition)
	if err != nil {
		return nil, err
	}
	if options.DependencySource == "" {
		options.DependencySource = DependencySourceRegistry
	}
	transports, err := normalizeTransports(options.Transports)
	if err != nil {
		return nil, err
	}
	if options.Port == 0 {
		options.Port = defaultGeneratorPort
	}

	projectName := strings.TrimSpace(options.ProjectName)
	if projectName == "" {
		projectName = csharpProjectName(model.SystemName)
	}
	rootNamespace := strings.TrimSpace(options.RootNamespace)
	if rootNamespace == "" {
		rootNamespace = csharpNamespace(projectName)
	}
	if err := validateDependencySource(options.DependencySource); err != nil {
		return nil, err
	}
	if err := validateGeneratedPort(options.Port); err != nil {
		return nil, err
	}
	if err := validateCSharpProjectName(projectName); err != nil {
		return nil, err
	}
	if err := validateCSharpNamespace(rootNamespace); err != nil {
		return nil, err
	}
	testsProjectName := projectName + ".Tests"

	files := []GeneratedFile{
		{Path: projectName + ".csproj", Content: buildGeneratedCSharpProjectFile(options.DependencySource, transports)},
		{Path: "README.md", Content: buildGeneratedCSharpReadme(model.SystemName, options.DependencySource, transports)},
		{Path: "appsettings.json", Content: buildGeneratedCSharpAppSettings(options.Port)},
		{Path: "anip-service-definition.json", Content: string(model.DefinitionJSON)},
		{Path: "Program.cs", Content: buildGeneratedCSharpProgram(rootNamespace, model.SystemName, transports)},
		{Path: "GeneratedRuntimeTarget.cs", Content: buildGeneratedCSharpRuntimeTarget(rootNamespace, string(model.RuntimeTargetJSON), string(model.CapabilitiesJSON), fallbackString(definition.ContractSchemaVersion, "generated-1"))},
		{Path: "BackendAdapter.cs", Content: buildGeneratedCSharpBackendAdapter(rootNamespace)},
		{Path: "Policy.cs", Content: buildGeneratedCSharpPolicy(rootNamespace)},
		{Path: "GeneratedCapabilities.cs", Content: buildGeneratedCSharpCapabilities(rootNamespace)},
		{Path: filepath.ToSlash(filepath.Join("tests", testsProjectName+".csproj")), Content: buildGeneratedCSharpTestsProjectFile(projectName, options.DependencySource)},
		{Path: filepath.ToSlash(filepath.Join("tests", "GeneratedCapabilitiesTests.cs")), Content: buildGeneratedCSharpTests(rootNamespace, firstGeneratedCapabilityID(model))},
	}
	files = append(files, buildIntegrationFrontingArtifacts(model)...)
	project := &GeneratedProject{
		PackageName: projectName,
		SystemName:  model.SystemName,
		Transports:  TransportNames(transports),
		CustomBundleTemplateValues: map[string]string{
			"CSHARP_ROOT_NAMESPACE": rootNamespace,
			"PACKAGE_NAME":          projectName,
		},
		Files: files,
	}
	return project, nil
}

func buildGeneratedCSharpProjectFile(dependencySource DependencySource, transports []Transport) string {
	lines := []string{
		`<Project Sdk="Microsoft.NET.Sdk.Web">`,
		`  <PropertyGroup>`,
		`    <TargetFramework>net10.0</TargetFramework>`,
		`    <Nullable>enable</Nullable>`,
		`    <ImplicitUsings>enable</ImplicitUsings>`,
		`  </PropertyGroup>`,
		"",
		`  <ItemGroup>`,
		`    <Compile Remove="tests/**/*.cs" />`,
		`    <None Include="tests/**/*.cs" />`,
		`  </ItemGroup>`,
		"",
	}
	if dependencySource == DependencySourceLocal {
		lines = append(lines, `  <ItemGroup>`)
		for _, reference := range []string{
			"Anip.Core/Anip.Core.csproj",
			"Anip.Crypto/Anip.Crypto.csproj",
			"Anip.Server/Anip.Server.csproj",
			"Anip.Service/Anip.Service.csproj",
			"Anip.AspNetCore/Anip.AspNetCore.csproj",
			"Anip.Rest.AspNetCore/Anip.Rest.AspNetCore.csproj",
			"Anip.GraphQL.AspNetCore/Anip.GraphQL.AspNetCore.csproj",
			"Anip.Mcp.AspNetCore/Anip.Mcp.AspNetCore.csproj",
		} {
			lines = append(lines, `    <ProjectReference Include="`+localCSharpProjectReference(reference)+`" />`)
		}
		if hasTransport(transports, TransportStdio) {
			lines = append(lines, `    <ProjectReference Include="`+localCSharpProjectReference("Anip.Stdio/Anip.Stdio.csproj")+`" />`)
		}
		lines = append(lines, `  </ItemGroup>`, "")
	} else {
		lines = append(lines,
			`  <PropertyGroup>`,
			`    <AnipVersion>`+anipCSharpPackageVersion+`</AnipVersion>`,
			`  </PropertyGroup>`,
			`  <ItemGroup>`,
			`    <PackageReference Include="Anip.Core" Version="$(AnipVersion)" />`,
			`    <PackageReference Include="Anip.Crypto" Version="$(AnipVersion)" />`,
			`    <PackageReference Include="Anip.Server" Version="$(AnipVersion)" />`,
			`    <PackageReference Include="Anip.Service" Version="$(AnipVersion)" />`,
			`    <PackageReference Include="Anip.AspNetCore" Version="$(AnipVersion)" />`,
			`    <PackageReference Include="Anip.Rest.AspNetCore" Version="$(AnipVersion)" />`,
			`    <PackageReference Include="Anip.GraphQL.AspNetCore" Version="$(AnipVersion)" />`,
			`    <PackageReference Include="Anip.Mcp.AspNetCore" Version="$(AnipVersion)" />`,
		)
		if hasTransport(transports, TransportStdio) {
			lines = append(lines, `    <PackageReference Include="Anip.Stdio" Version="$(AnipVersion)" />`)
		}
		lines = append(lines, `  </ItemGroup>`, "")
	}
	lines = append(lines, `</Project>`, "")
	return strings.Join(lines, "\n")
}

