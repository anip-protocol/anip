func buildGeneratedCSharpReadme(systemName string, dependencySource DependencySource, transports []Transport) string {
	lines := []string{
		"# " + titleCase(strings.NewReplacer("-", " ", "_", " ").Replace(systemName)),
		"",
		"Generated by `anip generate --target csharp` from an exported `anip-service-definition.json`.",
		"",
		"## What is generated",
		"",
		"- ASP.NET Core ANIP host using the repo's `Anip.*` runtime packages",
		"- generated capability declarations from the shared Go generation model",
		"- explicit backend adapter and policy seams for handwritten completion",
		"- xUnit smoke test covering capability generation",
		"",
		"## Commands",
		"",
	}
	if dependencySource == DependencySourceLocal {
		lines = append(lines,
			"- `dotnet run --project ./"+csharpProjectName(systemName)+".csproj`",
			"- `dotnet test ./tests/"+csharpProjectName(systemName)+".Tests.csproj`",
		)
	} else {
		lines = append(lines,
			"- `dotnet run --project ./"+csharpProjectName(systemName)+".csproj`",
			"- `dotnet test ./tests/"+csharpProjectName(systemName)+".Tests.csproj`",
		)
	}
	if hasTransport(transports, TransportStdio) {
		lines = append(lines, "- `dotnet run --project ./"+csharpProjectName(systemName)+".csproj -- --stdio`")
	}
	lines = append(lines, "")
	return strings.Join(lines, "\n")
}

