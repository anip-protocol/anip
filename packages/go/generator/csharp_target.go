func csharpProjectName(systemName string) string {
	base := titleCase(strings.NewReplacer("-", " ", "_", " ", ".", " ").Replace(systemName))
	base = strings.ReplaceAll(base, " ", "")
	if base == "" {
		return "GeneratedAnipService"
	}
	return base
}

func csharpNamespace(projectName string) string {
	base := strings.TrimSpace(projectName)
	if base == "" {
		base = "GeneratedAnipService"
	}
	segments := strings.Split(base, ".")
	normalized := make([]string, 0, len(segments))
	for _, segment := range segments {
		var builder strings.Builder
		for index, r := range segment {
			if (r >= 'A' && r <= 'Z') || (r >= 'a' && r <= 'z') || (r >= '0' && r <= '9') {
				if index == 0 && r >= '0' && r <= '9' {
					builder.WriteString("Generated")
				}
				builder.WriteRune(r)
			}
		}
		part := builder.String()
		if part != "" {
			normalized = append(normalized, part)
		}
	}
	result := strings.Join(normalized, ".")
	if result == "" {
		return "GeneratedAnipService"
	}
	return result
}

