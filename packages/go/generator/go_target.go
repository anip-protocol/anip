func BuildGoProject(definition *AnipServiceDefinition, options BuildGoProjectOptions) (*GeneratedProject, error) {
	model, err := BuildGenerationModel(definition)
	if err != nil {
		return nil, err
	}
	if options.DependencySource == "" {
		options.DependencySource = DependencySourceRegistry
	}
	transports, err := normalizeTransports(options.Transports)
	if err != nil {
		return nil, err
	}
	if options.Port == 0 {
		options.Port = defaultGeneratorPort
	}

	systemName := strings.TrimSpace(model.SystemName)
	modulePath := strings.TrimSpace(options.ModulePath)
	if modulePath == "" {
		modulePath = "generated/" + systemNameToPackageName(systemName)
	}
	if err := validateDependencySource(options.DependencySource); err != nil {
		return nil, err
	}
	if err := validateGeneratedPort(options.Port); err != nil {
		return nil, err
	}
	if err := validateGoModulePath(modulePath); err != nil {
		return nil, err
	}

	files := []GeneratedFile{
		{Path: "go.mod", Content: buildGeneratedGoMod(modulePath, options.DependencySource)},
		{Path: "README.md", Content: buildGeneratedGoReadme(systemName, options.DependencySource, transports)},
		{Path: "anip-service-definition.json", Content: string(model.DefinitionJSON)},
		{Path: "generated/runtime_target.go", Content: buildGeneratedGoRuntimeTargetModule(string(model.RuntimeTargetJSON), string(model.CapabilitiesJSON))},
		{Path: "host/capabilities.go", Content: buildGeneratedGoCapabilitiesModule(modulePath)},
		{Path: "extensions/backend_adapter.go", Content: buildGeneratedGoBackendAdapterModule(modulePath)},
		{Path: "extensions/policy.go", Content: buildGeneratedGoPolicyModule(modulePath)},
		{Path: "app/service.go", Content: buildGeneratedGoAppModule(modulePath)},
		{Path: "main.go", Content: buildGeneratedGoMainModule(modulePath, options.Port, systemName, transports)},
		{Path: "app/service_smoke_test.go", Content: buildGeneratedGoSmokeTestModule(modulePath)},
	}
	if options.DependencySource == DependencySourceLocal {
		files = append(files, GeneratedFile{Path: "go.work", Content: buildGeneratedGoWorkspace()})
	}
	files = append(files, buildIntegrationFrontingArtifacts(model)...)

	project := &GeneratedProject{
		PackageName: systemNameToPackageName(systemName),
		SystemName:  systemName,
		Transports:  TransportNames(transports),
		CustomBundleTemplateValues: map[string]string{
			"GO_MODULE_PATH": modulePath,
		},
		Files: files,
	}
	return project, nil
}

func buildGeneratedGoMod(modulePath string, dependencySource DependencySource) string {
	lines := []string{
		"module " + modulePath,
		"",
		"go 1.25.0",
		"",
		"require github.com/anip-protocol/anip/packages/go v0.24.7",
	}
	if dependencySource == DependencySourceLocal {
		lines = append(lines,
			"",
			"replace github.com/anip-protocol/anip/packages/go => "+localGoDependencyPath(),
		)
	}
	lines = append(lines, "")
	return strings.Join(lines, "\n")
}

func buildGeneratedGoWorkspace() string {
	return strings.Join([]string{
		"go 1.25.0",
		"",
		"use (",
		"	.",
		"	" + localGoDependencyPath(),
		")",
		"",
	}, "\n")
}

func buildGeneratedGoReadme(systemName string, dependencySource DependencySource, transports []Transport) string {
	lines := []string{
		"# " + titleCase(strings.NewReplacer("-", " ", "_", " ").Replace(systemName)),
		"",
		"Generated by `anip generate --target go` from an exported `anip-service-definition.json`.",
		"",
		"## What is generated",
		"",
		"- ANIP HTTP host using the Go `service` and `httpapi` packages",
		"- generated capability declarations and handler routing",
		"- explicit backend adapter and policy seams for handwritten completion",
		"- smoke test covering discovery and first-capability invocation",
		"",
		"## Extension seams",
		"",
		"- `extensions/backend_adapter.go` owns provider-specific backend execution",
		"- `extensions/policy.go` owns policy decisions that cannot remain declarative",
		"",
		"## Commands",
		"",
		"- `go run .`",
		"- `go test ./...`",
	}
	if hasTransport(transports, TransportStdio) {
		lines = append(lines, "- `go run . --stdio`")
	}
	lines = append(lines, "")
	if dependencySource == DependencySourceLocal {
		lines = append(lines,
			"## Local dependency mode",
			"",
			"`go.mod` uses a local `replace` for the ANIP Go runtime and `go.work` includes both this generated module and the local ANIP Go module so tests can resolve transitive runtime checksums.",
			"",
		)
	}
	return strings.Join(lines, "\n")
}

